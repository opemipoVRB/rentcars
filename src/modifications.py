from pydantic import BaseModel, EmailStr

from src.models import Customer, NotFound


class UpdateCustomerCommand(BaseModel):
    id: int
    update: dict

    def execute(self) -> Customer:
        customer = Customer.update_customer(id=self.id, updates=self.update)
        return customer


class DeleteCustomer(BaseModel):
    id: str

    def execute(self) -> Customer:
        customer = Customer.delete_customer(self.id)
        return customer
